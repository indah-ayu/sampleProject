    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        PreparedStatement stat;
        ResultSet rs;
        String jenis_kelamin = "";
        String hobi;
        Connection con = null;
        try 
        {
            con = koneksi.getConnection();
            String SqlCari = "Select * from Data_Siswa where No_Induk=?";
            stat = con.prepareStatement(SqlCari);
            String No_Induk = jTextField2.getText();
            stat.setString(1, No_Induk);
            rs = stat.executeQuery();
            rs.next();
            jTextField1.setText(rs.getString("Nama").trim());
            jTextArea1.setText(rs.getString("Alamat").trim());
            jenis_kelamin = rs.getString("Jenis_Kelamin").trim();
            hobi = rs.getString("hobi").trim();
            if (jenis_kelamin.equals("Laki-laki")) 
            {
                jRadioButton1.setSelected(true);
            }
            if (jenis_kelamin.equals("Perempuan")) 
            {
                jRadioButton2.setSelected(true);
            }
            if (hobi.equals("Hobi 1")) 
            {
                jCheckBox1.setSelected(true);
            }
            if (hobi.equals("Hobi 2")) 
            {
                jCheckBox2.setSelected(true);
            }
            if (hobi.equals("Hobi 1 Hobi 2")) 
            {
                jCheckBox1.setSelected(true);
                jCheckBox2.setSelected(true);
            }
            String Jurusan = rs.getString("Jurusan").trim();
            String Asal_Sekolah = rs.getString("Asal_Sekolah").trim();
            if (Jurusan.equals("IPA")) 
            {
                jComboBox1.setSelectedIndex(0);
            } 
            else if (Jurusan.equals("IPS")) 
            {
                jComboBox1.setSelectedIndex(1);
            } 
            else 
            {
                jComboBox1.setSelectedIndex(2);
            }
            if (Asal_Sekolah.equals("SMU")) 
            {
                jList1.setSelectedIndex(0);
            } 
            else if (Asal_Sekolah.equals("SMK")) 
            {
                jList1.setSelectedIndex(1);
            } 
            else 
            {
                jList1.setSelectedIndex(2);
            }
        } 
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(null, ex.getMessage().toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

