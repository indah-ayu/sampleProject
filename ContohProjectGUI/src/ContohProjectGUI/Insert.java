public String generateCode()
{ 
    Statement st;
    ResultSet rs;
    String No_Induk = "";
    Connection con = null;
    String kode = "";   
    int newCode=0;
    try 
    {
        con = koneksi.getConnection();
        String view = "select Top 1 No_Induk from Data_Siswa order by No_Induk desc";
        st = con.createStatement();
        rs = st.executeQuery(view);        
        while (rs.next()) 
        {              
            No_Induk = rs.getString("No_Induk") ;       
        }            
    } 
    catch (Exception ex) 
    {
        JOptionPane.showMessageDialog(null, ex.getMessage().toString());
    }
    if (No_Induk.equals(""))
    {
        kode = "M001";
    }
    else
    {
        kode = No_Induk.substring(1, 4);
        newCode = Integer.parseInt(kode);
        if(newCode < 9)
        {
            newCode = newCode + 1;
            kode = "M00" + newCode;
        }
        else if(newCode < 99)
        {
            newCode = newCode + 1;
            kode = "M0" + newCode;
        }
        else 
        {
            newCode = newCode + 1;
            kode = "M" + newCode;
        } 
    }  
    return kode;
}
